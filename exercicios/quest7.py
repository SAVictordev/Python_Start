## Faça um algoritmo que leia dois valores booleanos (lógicos) e determine se ambos são VERDADEIRO ou FALSO.

val1 = True
val2 = False

if (val1 == True and val2 == True):
    print("Ambos são verdadeiros")
elif (val1 == False and val2 == False):
    print("Ambos são falsos")
else:
    print("Um é falso e outro verdadeiro")
