## Operadores aritiméticos

num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

## Exemplo de soma e subtração

sum = num1 + num2
sub = num1 - num2

print("O Resultado da soma é: ", sum)
print("O Resultado da subtração é: ", sub)

## Exemplo de multiplicação e divisão

mult = num1 * num2
division = num1 / num2

print("O Resultado da multiplicação é: ", mult)
print("O Resultado da divisão é: ", division)


## Exemplo de módulo e parte inteira da divisão

mod = num1 % num2
intdiv = num1 // num2

print("O Resultado do módulo é:  ", mod)
print("O Resultado da divisão inteira é: ", intdiv)